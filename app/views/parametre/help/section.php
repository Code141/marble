  B - Choix des canaux<br>

  - site web:<br>
  Conpte tenu de la distance séparant les différents acteurs du groupe, de la fréquence d’utilisation, de l’envie de partager, du nombre de modifications qui sera apportées et de l’envie de se faire connaître, ce canal est incontournable.<br>

  - application smartphone :<br>
  Se voudra être un accès rapide aux nouveautés, elle intégrera les outils de lectures de flux, de partage rapide et de communication. Limitée dans les fonctionalités avancées de recherches structurées<br>

  - réseaux sociaux :<br>
  Marble étant potentielement un réseau social il doit proposer le même genre d’outils que sur ceux existant, néanmoins, afin de se faire connaître, ils parait inévitable de créer une communauté de groupes de fans qui suivraient le projet dans son avancement, et aideraient à l’avancement par leurs critiques.<br>

  - e-mailing :<br>
  Ce canal ne sera utilisé qu’à titre administratif. Enregistrement, changement de mot de pass etc...<br>

  - module web :<br>
  Simplifier les acteurs concernés ou externes à interagir plus facilement avec Marble au sein d’autres sites, via des petits modules web (like, dropzone etc...)<br>
