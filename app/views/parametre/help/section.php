  Introduction<br>
  Le projet Marble est un outil qui a pour but de simuler certaines parcelles de
  la facultée humaine représentatrice des classements d’informations pour constituer
  une banque objective de trie par la somme des visions subjectives utilisateurs.<br>
  <br>
  I     Stratégie<br>
  <br>
  A - Qui est le client ?<br>

  Le client est a court terme tout initié sensible au projet, Marble étant un projet d’étude expérimental, son développement n’est pas axé sur une demande client particulière. Néanmoins, si le prototypage de ce projet fonctionne a la hauteur des attentes, il se vera être adapté pour une utilisation par les grandes masses, mais naturellement dans ce cas, une insertion marché par le biais des utilisateurs expérimentés en terme de nouvelles technologies en vue de mettre sa viabilité a l’épreuve avant d'être réellement démocratisé devra etre envisagé.<br>

  B - Quelles sont les problématiques a résoudre ?<br>

  Marble cherche a régler diverses problématiques liées au support web, son exploration, ainsi que la récolte et le classement d’information de manière subjective.<br>

  Le but est d’organiser l’information de manière pluri-dimensionnel en se basant sur un contexte des subjectivités massives, spatialisé par les jugements de valeurs grâce a l'attribution de qualifiants de degrés affectifs variants. Chaque utilisateur établirai de nouveaux liens (ici, vecteurs), dans le but de se constituer une cartographie unique, personnelle et pertinente dans le ”marbre”.<br>

  Ce qui est donc révélé par ce projet n’est pas l’importance des liens en eux-mêmes que possède divers utilisateur face a divers objet, mais bien le “type” de lien qu’ils entretiennent avec. Plus précisément, Marble a également donc pour mission d’avoir la capacité de géolocaliser le plus précisément possible dans un environnement à n dimension, des utilisateurs ou des objets quels qu’ils soient, et en déterminer a souhait les liens possibles d’après leur position et leur orientation.<br>

  Il doit également enrayer la tendance de linéarisation temporelle du media web avant qu’elle ne se généralise, en se proposant comme étant une preuve que la puissance d’Internet réside bien dans son aspect inter-dimensionnel et donc dans sa faculté à se présenter comme une multitude d’entités entrelacées.<br>

